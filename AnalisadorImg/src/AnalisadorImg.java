    private void jToggleButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton1ActionPerformed
        JFileChooser chooser = new JFileChooser();
        chooser.setCurrentDirectory(new java.io.File("."));
        chooser.setDialogTitle("Escolha a pasta do projeto");
        chooser.setFileSelectionMode(JFileChooser.DIRECTORIES_ONLY);

        if(chooser.showOpenDialog(this) == JFileChooser.APPROVE_OPTION) {
            prjPath = chooser.getSelectedFile();
            // vamos agr desbloquear os demais componentes

            // feito isso
            // vamos zerar o componente de lista
            DefaultListModel l = new DefaultListModel<String>();

            File[] listOfFiles = prjPath.listFiles();

            for(File file : listOfFiles) {
                if(file.isFile()) {
                    System.out.println("File " + file.getName());
                    if(file.getName().matches(".*\\.bmp$")) {
                        l.addElement(file.getName());
                    }
                }
            }

            fileList.setModel(l);
        }
    }//GEN-LAST:event_jToggleButton1ActionPerformed

