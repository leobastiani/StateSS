    public static byte[] intToRgb(int rgb) {
        final byte r = (byte) ((rgb >> 16) & 0xFF);
        final byte g = (byte) ((rgb >> 8) & 0xFF);
        final byte b = (byte) (rgb & 0xFF);
        final byte[] res = new byte[] {
            r,
            g,
            b
        };
        return res;
    }

