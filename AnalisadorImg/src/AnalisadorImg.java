    private void imgLabelMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_imgLabelMouseClicked
        // ao clicar, pega o valor x e y do click
        int x = evt.getX();
        int y = evt.getY();
        clicked = new XY(x, y);
        Graphics g = imgLabel.getGraphics();
        /*g.fillRect(0, 0, 200, 200);
        byte[] rgb = intToRgb(image.getRGB(100, 100));
        System.out.println(rgb[0]);
        System.out.println(rgb[1]);
        System.out.println(rgb[2]);*/
        // vamos pegar todos os pixels que vão ter seus valores lidos
        ArrayList<Pixel> listPixels = new ArrayList<Pixel>();
        listPixels.add(new Pixel(x, y, image));
        for(int i = 1; i <= Integer.valueOf(qntPixels.getText()); i++) {
            if(vertical.isSelected()) {
                listPixels.add(new Pixel(x, y + i, image));
                listPixels.add(new Pixel(x, y - i, image));
            } else {
                listPixels.add(new Pixel(x + i, y, image));
                listPixels.add(new Pixel(x - i, y, image));
            }
        }
        // listo todos eles
        // e pinto
        StringBuilder sb = new StringBuilder();
        for(Pixel p : listPixels) {
            System.out.println(p);
            // vamos pintar com o pixel ao contrário
            g.setColor(new Color(255 - p.getR(), 255 - p.getG(), 255 - p.getB()));
            g.fillRect(p.getXy().getX(), p.getXy().getY(), 1, 1);
            sb.append(p.getXy().getX() + " " + p.getXy().getY() + " " + p.getR() + " " + p.getG() + " " + p.getB() + "\r\n");
        }
        try {
            // salvo num arquivo
            Pattern p = Pattern.compile("(.*)\\.bmp");
            Matcher m = p.matcher(fileList.getSelectedValue());
            m.find();
            FileWriter f = new FileWriter(prjPath.getAbsolutePath() + "\\" + m.group(1) + ".txt");
            f.write(sb.toString());
            f.close();
        } catch(IOException ex) {
            Logger.getLogger(AnalisadorImg.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_imgLabelMouseClicked

